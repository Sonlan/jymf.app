package org.jymf.utils;

/**
 * 系统配置信息Bean
 * 
 * @author
 */
public class SysConfig {
    
    private String ip;
    
	private String port;
	
	private String url;

    public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}
}
